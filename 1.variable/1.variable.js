/**
 * 단일 데이터 - 원시 (primitive) : number, string, boolen, null, undefined, Symbol
 * Copy by Value : 값을 복사함
 */
/**
 * 복합 데이터 - 객체 object : 상태 property, 행동 method
 * Copy by Reference
 */
// let은 재할당 가능, const 재할당 불가능
// 변수 Variable : 값을 저장하는 공간, 자료를 저장할 수 있는 이름이 주어진 기억 장소
let a = 0;
console.log(a);

a = 1;
console.log(a);

let b;
console.log(b);

b = 2;
console.log(b);
